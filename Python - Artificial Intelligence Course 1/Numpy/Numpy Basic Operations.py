import numpy as np

# Numpy Basic Operators
array1 = np.array([1, 2, 3, 4])
array2 = np.array([9, 8, 7, 6])

print("add: ", array1 + array2)
print("sub: ", array1 - array2)
print("sqr: ", array1 ** array2)

print("sin: ", np.sin(array1))

print("Comparison Operators: ", array1 < 2)

